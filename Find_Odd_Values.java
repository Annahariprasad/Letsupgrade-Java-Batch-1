public class Find_Odd_Values {

		public static void main(String[] args){
		    int array[] = new int[5];
		    array[0] = 89; array[1] = 74; array[2] = 27; array[3] = 62; array[4] = 97;
		    System.out.print("Odd Values : ");
		    for(int i=0; i<5; i++){
		        if(array[i]%2!=0){
		            System.out.print(array[i]+" ");
		        }
  }
		}
}

